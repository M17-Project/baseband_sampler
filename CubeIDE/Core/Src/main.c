void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	//CONVST
	if(HAL_GPIO_ReadPin(CONVST_GPIO_Port, CONVST_Pin)==0 && sampling && ready)
	{
		HAL_SPI_Receive(&hspi1, val, 2, 1);
		data[cnt]=(val[1]<<8)|val[0];
		cnt++;

		if(cnt>=NUM_SAMPLES)
		{
			sampling=0;
			cnt=0;
			done=1;
		}
	}

	//blue button
	else if(HAL_GPIO_ReadPin(BTN_GPIO_Port, BTN_Pin)==0)
	{
		cal_pending=1;
		//sampling=1;
	}
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(sampling==0 && done==0 && cnt==0)
		sampling=1;
	HAL_UART_Receive_IT(&huart2, cal, 1); //destination doesnt matter
}
